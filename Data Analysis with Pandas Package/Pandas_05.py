# Python for statistics

# importing the required module
from statistics import mean

# statement will print the mean of the numbers
print(mean([1,2,2,2,1,3,4,1,5]))


from statistics import median

#  statement will print the median (center number)
print(median([1,1,1,2,2]))


from statistics import mode

# statement will print the most common number
print(mode([1,1,1,2,2]))








